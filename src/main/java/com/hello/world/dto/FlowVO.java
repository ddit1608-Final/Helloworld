package com.hello.world.dto;

public class FlowVO {
	private String flow_code;
	private String flow_id;
	private String flow_cont;

	public String getFlow_code() {
		return flow_code;
	}

	public void setFlow_code(String flow_code) {
		this.flow_code = flow_code;
	}

	public String getFlow_id() {
		return flow_id;
	}

	public void setFlow_id(String flow_id) {
		this.flow_id = flow_id;
	}

	public String getFlow_cont() {
		return flow_cont;
	}

	public void setFlow_cont(String flow_cont) {
		this.flow_cont = flow_cont;
	}

}
